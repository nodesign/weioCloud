$( document ).ready(function() {
    // Get country list
    $.getJSON('countries.json', function(data) {
            var confFile = data;
            //console.log(confFile[0][1]);
            
            $("#countries").empty();
            for (var i=0; i<confFile.length; i++) {
                $("#countries").append('<option value="'+confFile[i][1]+'">'+confFile[i][0]+'</option>');
            }
    });

});

